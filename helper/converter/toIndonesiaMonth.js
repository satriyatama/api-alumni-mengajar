module.exports = toIndonesiaMonth = (month) => {
  let indonesiaMonth
  switch (month) {
    case 'January':
        indonesiaMonth = 'Januari'
      break;
    case 'February':
        indonesiaMonth = 'Februari'
      break;
    case 'March':
        indonesiaMonth = 'Maret'
      break;
    case 'April':
        indonesiaMonth = 'April'
      break;
    case 'May':
        indonesiaMonth = 'Mei'
      break;
    case 'June':
        indonesiaMonth = 'Juni'
      break;
    case 'July':
        indonesiaMonth = 'Juli'
      break;
    case 'August':
        indonesiaMonth = 'Agustus'
      break;
    case 'September':
        indonesiaMonth = 'September'
      break;
    case 'October':
        indonesiaMonth = 'Oktober'
      break;
    case 'November':
        indonesiaMonth = 'November'
      break;
    case 'December':
        indonesiaMonth = 'Desember'
      break;
  }
  return indonesiaMonth
}